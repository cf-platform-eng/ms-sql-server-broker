package io.pivotal.ecosystem.sqlserver.connector;

import lombok.extern.slf4j.Slf4j;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.PropertySource;
import org.springframework.core.env.Environment;

@Configuration
@PropertySource("classpath:application.properties")
@Slf4j
class TestConfig {

    @Bean
    public SqlServerServiceInfo sqlServerServiceInfo(Environment env) {
        return new SqlServerServiceInfo("1", env.getProperty(SqlServerServiceInfo.USER_KEY), env.getProperty(SqlServerServiceInfo.PW_KEY), SqlServerServiceInfo.URI_SCHEME + "://" + env.getProperty(SqlServerServiceInfo.HOST_KEY) + ":" + env.getProperty(SqlServerServiceInfo.PORT_KEY));
    }
}